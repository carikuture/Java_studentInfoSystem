    private void buttonAddCourseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAddCourseActionPerformed
        // TODO add your handling code here:
        int input = javax.swing.JOptionPane.showConfirmDialog(rootPane, "Add " + (String) comboCoursesAvailable.getSelectedItem() + " to Enrolled Subjects?");
        //0=yes, 1=no, 2=cancel
        if (input == 0) {
            DefaultTableModel model = (DefaultTableModel)tableEnrolledSubjects.getModel();
            List<String> courses = new ArrayList<String>();

            courses.add((String) comboCoursesAvailable.getSelectedItem());
            model.addRow(courses.toArray());

            tableEnrolledSubjects.setModel(model);
        } else {
            this.setVisible(true);
        }
    }//GEN-LAST:event_buttonAddCourseActionPerformed

    private void buttonRemoveCourseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonRemoveCourseActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)tableEnrolledSubjects.getModel();
        
        if (tableEnrolledSubjects.getSelectedRowCount()==1) {
            //if single row is selected, then delete
            model.removeRow(tableEnrolledSubjects.getSelectedRow());
        } else if (tableEnrolledSubjects.getRowCount()==0) {
            //if table is empty
            javax.swing.JOptionPane.showMessageDialog(rootPane, "No courses available.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        } else {
            javax.swing.JOptionPane.showMessageDialog(rootPane, "Please select Course.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_buttonRemoveCourseActionPerformed

