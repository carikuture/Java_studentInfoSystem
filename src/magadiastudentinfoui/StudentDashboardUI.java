    //displaying courses in "Courses Avaiallaeble" combo box
    public void InitializeAndSetCourses() {
        DataHandler.Students.CreateSubjects();
        
        for(Subject s:DataHandler.Students.availableSubjects) {
            String item = s.getCode() + " - " + s.getName() + " - " + s.getInstructor();
            comboCoursesAvailable.addItem(item);
        }
    }
    
    //displaying courses in "Enrolled Subecjst" table
    public void SetEnrolledCourses() {
        LinkedList<Subject> subjectsEnrolled = DataHandler.Students.sampleStudent.getSubjectsEnrolled();
        DefaultTableModel model = (DefaultTableModel)tableEnrolledSubjects.getModel();
        
        for(Subject s:subjectsEnrolled) {
            model.addRow( 
                new Object[] {
                    s.getCode(),
                    s.getName(),
                    s.getInstructor(),
                    s.getStart() + " - " + s.getEnd()
                }
            );
        }
        
        tableEnrolledSubjects.setModel(model);
    }

